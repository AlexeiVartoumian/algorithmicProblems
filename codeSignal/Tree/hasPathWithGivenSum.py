"""
dfs question. recursively apply the following, if we reached a NUll Node
then go back up to last viable node. if current viable node has no children check if current accumalted sum is plus value stored in current node is equal to target sum. return true if so.
oterhwise accumalate tsrget sum returning either left or right child sicne we want to explore all possible routes until our condition is met.
otherwise once all is traversed return false.
return the function which will return a type boolean.
"""

#
# Binary trees are already defined with this interface:
# class Tree(object):
#   def __init__(self, x):
#     self.value = x
#     self.left = None
#     self.right = None

def solution(t, s):
    
    
    
    def dfs(root,cursum):
        
        if not root:
            return False    
    
        elif not root.left and not root.right:
            
            if cursum + root.value == s:
                return True
        
        else:
            cursum+= root.value
            return dfs(root.left,cursum) or dfs(root.right,cursum) 
        
        return False
    
    return dfs(t,0)